package BasicsOfJava;

public class Arithematics {
    public static void main(String[] args) {
        float x = 5;
        float y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(y-x);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
