package BasicsOfJava;

public class PrintingNumbers {
    public static void main(String[] args) {
        int x;
        x = 400;
        System.out.println(x);
        x = 500;
        System.out.println(x);
        System.out.println(x+1);
        x = x + 5;
        System.out.println(x);
        x = x - 10;
        System.out.println(x);
    }
}
