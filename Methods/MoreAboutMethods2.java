package Methods;

public class MoreAboutMethods2 {
    public static void pehla() {
        for (int i = 0; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
    }

    public static void main(String[] args) {
        System.out.println("Yo Yo");
        pehla();
        System.out.println("Gian");
        dooshra();
        teeshra();

    }

    public static void teeshra() {
        System.out.println("kaisa h bro ?");
    }
    public static void dooshra() {
        System.out.println("Hi, Goodmorning");
    }
}
